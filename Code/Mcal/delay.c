#include "delay.h"

//-----------------------------------------------------------------------------------------
/// \brief  
///
/// \param  
///
/// \return 
//-----------------------------------------------------------------------------------------
void delayus(uint32_t us)
{
	while(us--)
	{
		/* 1 us busywait delay */
		for(int i=0;i<283;i++)
		{
		  __asm("nop");
		}
	}
}

//-----------------------------------------------------------------------------------------
/// \brief  
///
/// \param  
///
/// \return 
//-----------------------------------------------------------------------------------------
void delayms(uint32_t ms)
{
	while(ms--)
	{
		/* 1 ms busywait delay */
		for(int i=0;i<283360;i++)
		{
		  __asm("nop");
		}
	}
}

